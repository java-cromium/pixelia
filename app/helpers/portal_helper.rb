  def portal_nav_link(label, path, icon_key)
    active = current_page?(path)
    classes = if active
      "flex items-center gap-3 px-3 py-2.5 text-sm font-semibold text-cyan-400 bg-cyan-500/10 rounded-lg"
    else
      "flex items-center gap-3 px-3 py-2.5 text-sm text-slate-400 hover:text-cyan-400 hover:bg-slate-800 rounded-lg transition-colors"
    end

    link_to path, class: classes do
      icon_svg(icon_key) + content_tag(:span, label)
    end
  end

  private

  def icon_svg(key)
    path_data = PORTAL_ICONS[key] || ""
    content_tag(:svg, path_data.html_safe, class: "w-5 h-5 shrink-0", fill: "none", stroke: "currentColor", viewBox: "0 0 24 24").html_safe
  end
